package entidad;

public class EstadisticasPacientes {
	private String rangoEdad;
	private int cantidadRangoEdad;
	private int anioTurno;
	private int mesTurno;
	private String especialidadTurno;
	private int cantidadTurno;
	
	
		
	public EstadisticasPacientes() {

	}



	public String getRangoEdad() {
		return rangoEdad;
	}



	public void setRangoEdad(String rangoEdad) {
		this.rangoEdad = rangoEdad;
	}



	public int getCantidadRangoEdad() {
		return cantidadRangoEdad;
	}



	public void setCantidadRangoEdad(int cantidadRangoEdad) {
		this.cantidadRangoEdad = cantidadRangoEdad;
	}



	public int getAnioTurno() {
		return anioTurno;
	}



	public void setAnioTurno(int anioTurno) {
		this.anioTurno = anioTurno;
	}



	public int getMesTurno() {
		return mesTurno;
	}



	public void setMesTurno(int mesTurno) {
		this.mesTurno = mesTurno;
	}



	public String getEspecialidadTurno() {
		return especialidadTurno;
	}



	public void setEspecialidadTurno(String especialidadTurno) {
		this.especialidadTurno = especialidadTurno;
	}



	public int getCantidadTurno() {
		return cantidadTurno;
	}



	public void setCantidadTurno(int cantidadTurno) {
		this.cantidadTurno = cantidadTurno;
	}


	
}
